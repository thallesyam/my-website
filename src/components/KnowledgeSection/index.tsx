export function KnowledgeSection() {
  const [techHover, setTechHover] = useState('')

  function handleHover(event: any) {
    const techName: string = event?.target?.ariaLabel

    const description = techs.find(
      (tech) => tech.name === techName
    )?.description

    if (!description) return

    setTechHover(description)
  }

  return (
    <section className={style.experience}>
      <section>
        <h5>
          Conhecimentos <span>.</span>
        </h5>

        <div className={style.techs_container}>
          <p>{!!techHover ? techHover : '*Passe o mouse para ler o card*'}</p>

          <div className={style.card_container}>
            {techs.map((tech) => (
              <div
                key={tech.id}
                className={style.card}
                onMouseEnter={(event) => handleHover(event)}
                onMouseLeave={() => setTechHover('')}
                aria-label={tech.name}
              >
                <span>{tech.name}</span>
                {tech.icon}
              </div>
            ))}
          </div>
        </div>
      </section>
    </section>
  )
}
